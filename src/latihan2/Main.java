package latihan2;

public class Main {
    public static void main(String[] args) {
        int a=8;
        int c=12;
        System.out.println("a + c = " + (a+c));
        System.out.println("a - c = " + (a-c));
        System.out.println("a * c = " + (a*c));
        System.out.println("a / c = " + (a/c));
        System.out.println("a < c = " + (a<c));
        System.out.println("a > c = " + (a>c));
        System.out.println("a >= c = " + (a>=c));
        System.out.println("a <= c = " + (a<=c));
        System.out.println("a == c = " + (a==c));
    }
}


package latihan5;

public class Latihan_Decrement {
    public static void main(String[] args) {
        int i = 10;
        int j = 25;
        int I = i--;
        int J = --j;
        System.out.println("Nilai I adalah = " + I);
        System.out.println("Nilai i adalah = " + i);
        System.out.println("Nilai J adalah = " + J);
    }
}
